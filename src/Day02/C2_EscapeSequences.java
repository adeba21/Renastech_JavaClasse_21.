package Day02;

public class C2_EscapeSequences {

    public static void main (String [] args) {

        System.out.println("RenasTech");

        // \t text--- this will give an extra space in your code
        System.out.println("\tRenasTech");
        System.out.println("\t\t\tRenasTech");

        // \n means that it's new line----- text will be displayed as new line

        System.out.println("Hello World");
        System.out.println("Hello \n\tWorld");

        // \\ means that it will print single slash on \the console

        System.out.println("\\");

        System.out.println("/");

        // inside text you would like to use "" :
        System.out.println("Today is \"Saturday\"  ");

        // to have single quotation

        System.out.println("\"today is \'Saturday\' ");

    }
}

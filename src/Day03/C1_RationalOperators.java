package Day03;

public class C1_RationalOperators {

    public static void main(String[]args){

        // create variables

        int a=10;
        int b=15;

        System.out.println(a==b);

        // != not equal

        System.out.println(a!=b);

        // > and < operator

        System.out.println(a>b);
        System.out.println(a<b);

        // >= operator

        System.out.println(a>=b);
        System.out.println(a<=b);


    }
}
